//Given the days of the week in short format "Sun", "Mon" ... print in long format "Sunday", "Monday", ...
let day="sat";

if(day=="sun"){
  console.log("sunday");
}else if(day=="mon"){
  console.log("monday");
}else if(day=="tue"){
  console.log("tuesday");
}else if(day=="wed"){
  console.log("wednesday");
}else if(day=="thu"){
  console.log("thursday");
}else if(day=="fri"){
  console.log("friday");
}else if(day=="sat"){
  console.log("saturday");
}else{
  console.log("please write correct spelling");
}